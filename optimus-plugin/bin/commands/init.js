#!/usr/bin/env node

/**
 * `optimus init` — Bootstrap a .optimus/ workspace in the current directory.
 * 
 * Copies starter personas, config, and creates required subdirectories.
 * Appends .optimus ignore entries to .gitignore if not already present.
 */

const fs = require('fs');
const path = require('path');

function copyDirRecursive(src, dest) {
  if (!fs.existsSync(dest)) {
    fs.mkdirSync(dest, { recursive: true });
  }
  const entries = fs.readdirSync(src, { withFileTypes: true });
  for (const entry of entries) {
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);
    if (entry.isDirectory()) {
      copyDirRecursive(srcPath, destPath);
    } else {
      if (!fs.existsSync(destPath)) {
        fs.copyFileSync(srcPath, destPath);
        console.log(`  ✅ Created ${path.relative(process.cwd(), destPath)}`);
      } else {
        console.log(`  ⏭️  Skipped ${path.relative(process.cwd(), destPath)} (already exists)`);
      }
    }
  }
}

module.exports = function init() {
  const cwd = process.cwd();
  const optimusDir = path.join(cwd, '.optimus');
  const scaffoldDir = path.resolve(__dirname, '..', '..', 'scaffold');
  const pluginRoot = path.resolve(__dirname, '..', '..');

  console.log('\n🤖 Optimus Swarm — Initializing workspace...\n');

  // 1. Create required subdirectories
  const dirs = ['personas', 'config', 'skills', 'agents', 'tasks', 'reports', 'reviews', 'memory', 'state'];
  for (const dir of dirs) {
    const dirPath = path.join(optimusDir, dir);
    if (!fs.existsSync(dirPath)) {
      fs.mkdirSync(dirPath, { recursive: true });
      console.log(`  📁 Created .optimus/${dir}/`);
    }
  }

  // 2. Copy scaffold personas
  const personasSrc = path.join(scaffoldDir, 'personas');
  if (fs.existsSync(personasSrc)) {
    console.log('\n📋 Installing starter personas...');
    copyDirRecursive(personasSrc, path.join(optimusDir, 'personas'));
  }

  // 3. Copy scaffold config
  const configSrc = path.join(scaffoldDir, 'config');
  if (fs.existsSync(configSrc)) {
    console.log('\n⚙️  Installing system config...');
    copyDirRecursive(configSrc, path.join(optimusDir, 'config'));
  }

  // 3.5 Copy plugin skills (the "operation manuals" for MCP tools)
  const skillsSrc = path.join(pluginRoot, 'skills');
  if (fs.existsSync(skillsSrc)) {
    console.log('\n📚 Installing skills (MCP tool guides)...');
    copyDirRecursive(skillsSrc, path.join(optimusDir, 'skills'));
  }

  // 3.6 Copy plugin agents (T2 global agent definitions)
  const agentsSrc = path.join(pluginRoot, 'agents');
  if (fs.existsSync(agentsSrc)) {
    console.log('\n🤺 Installing global agents...');
    copyDirRecursive(agentsSrc, path.join(optimusDir, 'agents'));
  }

  // 4. Append to .gitignore if needed
  const gitignorePath = path.join(cwd, '.gitignore');
  const optIgnorePath = path.join(scaffoldDir, '.gitignore-optimus');
  if (fs.existsSync(optIgnorePath)) {
    const ignoreEntries = fs.readFileSync(optIgnorePath, 'utf8');
    let existingIgnore = '';
    if (fs.existsSync(gitignorePath)) {
      existingIgnore = fs.readFileSync(gitignorePath, 'utf8');
    }
    if (!existingIgnore.includes('.optimus/reports/')) {
      fs.appendFileSync(gitignorePath, '\n# Optimus Swarm generated artifacts\n' + ignoreEntries);
      console.log('\n📝 Updated .gitignore with Optimus entries');
    }
  }

  // 5. Bridge system instructions to detected AI client(s)
  const instructionsPath = path.join(optimusDir, 'config', 'system-instructions.md');
  if (fs.existsSync(instructionsPath)) {
    const instructions = fs.readFileSync(instructionsPath, 'utf8');
    const bridgeHeader = '<!-- Auto-generated by optimus init — source: .optimus/config/system-instructions.md -->\n\n';
    let bridged = [];

    // Detect Claude Code (CLAUDE.md at project root)
    const claudeMdPath = path.join(cwd, 'CLAUDE.md');
    if (fs.existsSync(path.join(cwd, '.claude')) || fs.existsSync(claudeMdPath)) {
      // Claude Code environment detected
      if (!fs.existsSync(claudeMdPath)) {
        fs.writeFileSync(claudeMdPath, bridgeHeader + instructions, 'utf8');
        bridged.push('CLAUDE.md (created)');
      } else {
        const existing = fs.readFileSync(claudeMdPath, 'utf8');
        if (!existing.includes('optimus')) {
          fs.appendFileSync(claudeMdPath, '\n\n' + bridgeHeader + instructions);
          bridged.push('CLAUDE.md (appended)');
        } else {
          bridged.push('CLAUDE.md (already contains optimus config)');
        }
      }
    }

    // Detect GitHub Copilot (.github/ directory)
    const copilotDir = path.join(cwd, '.github');
    const copilotPath = path.join(copilotDir, 'copilot-instructions.md');
    if (fs.existsSync(copilotDir)) {
      if (!fs.existsSync(copilotPath)) {
        fs.writeFileSync(copilotPath, bridgeHeader + instructions, 'utf8');
        bridged.push('.github/copilot-instructions.md (created)');
      } else {
        const existing = fs.readFileSync(copilotPath, 'utf8');
        if (!existing.includes('optimus')) {
          fs.appendFileSync(copilotPath, '\n\n' + bridgeHeader + instructions);
          bridged.push('.github/copilot-instructions.md (appended)');
        } else {
          bridged.push('.github/copilot-instructions.md (already contains optimus config)');
        }
      }
    }

    // Detect Cursor (.cursor/ directory)
    const cursorDir = path.join(cwd, '.cursor');
    if (fs.existsSync(cursorDir)) {
      const cursorRulesDir = path.join(cursorDir, 'rules');
      if (!fs.existsSync(cursorRulesDir)) {
        fs.mkdirSync(cursorRulesDir, { recursive: true });
      }
      const cursorRulePath = path.join(cursorRulesDir, 'optimus.mdc');
      fs.writeFileSync(cursorRulePath, bridgeHeader + instructions, 'utf8');
      bridged.push('.cursor/rules/optimus.mdc (created)');
    }

    // If no client detected, prompt user
    if (bridged.length === 0) {
      console.log('\n⚠️  No AI client config detected (.claude/, .github/, .cursor/).');
      console.log('   You can manually bridge the instructions:');
      console.log('     Claude Code:  cp .optimus/config/system-instructions.md CLAUDE.md');
      console.log('     Copilot:      cp .optimus/config/system-instructions.md .github/copilot-instructions.md');
      console.log('     Cursor:       cp .optimus/config/system-instructions.md .cursor/rules/optimus.mdc');
    } else {
      console.log('\n🔗 Bridged instructions to detected client(s):');
      for (const b of bridged) {
        console.log(`  → ${b}`);
      }
    }
  }

  console.log('\n✅ Workspace initialized! Your .optimus/ directory is ready.');
  console.log('   Run `optimus serve` or configure your MCP client to start.\n');
};
